class Solution(object):
    def depthSumInverse(self, nestedList):
        """
        :type nestedList: List[NestedInteger]
        :rtype: int
        """
        weighted = 0
        unweighted = 0
        while nestedList:
            tempList = []
            for element in nestedList:
                if element.isInteger():
                    unweighted += element.getInteger()
                else:
                    tempList += element.getList()
            weighted += unweighted
            nestedList = tempList
        return weighted
